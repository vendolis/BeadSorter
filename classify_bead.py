K_NEIGHBORS = 3


def _rgb_to_hls(r, g, b):
    """Convert RGB (0-1) to HLS. Returns (h, l, s)."""
    max_c = max(r, g, b)
    min_c = min(r, g, b)
    l = (max_c + min_c) / 2.0

    if max_c == min_c:
        return (0.0, l, 0.0)

    d = max_c - min_c
    s = d / (2.0 - max_c - min_c) if l > 0.5 else d / (max_c + min_c)

    if max_c == r:
        h = (g - b) / d + (6.0 if g < b else 0.0)
    elif max_c == g:
        h = (b - r) / d + 2.0
    else:
        h = (r - g) / d + 4.0
    h /= 6.0

    return (h, l, s)


def classify_bead(raw_r, raw_g, raw_b, raw_c):
    """
    Classify a bead from raw TCS34725 sensor values.

    Args:
        raw_r, raw_g, raw_b, raw_c: Raw RGBC readings from the sensor.

    Returns:
        Cluster ID (0-37), or -1 if the reading looks like an empty chamber.
    """
    # Subtract ambient light baseline
    r_corr = max(raw_r - NULL_R, 1.0)
    g_corr = max(raw_g - NULL_G, 1.0)
    b_corr = max(raw_b - NULL_B, 1.0)

    # Check if this is an empty chamber (values close to null baseline)
    if r_corr + g_corr + b_corr < 50:
        return -1

    # Scale to 0-1
    r01 = r_corr / RGB_MAX
    g01 = g_corr / RGB_MAX
    b01 = b_corr / RGB_MAX

    # Convert to HLS
    h, l, s = _rgb_to_hls(r01, g01, b01)

    # Encode hue on unit circle
    h_sin = math.sin(2.0 * math.pi * h)
    h_cos = math.cos(2.0 * math.pi * h)

    # Normalize features
    h_sin_n = (h_sin - NORM_H_SIN[0]) / NORM_H_SIN[1]
    h_cos_n = (h_cos - NORM_H_COS[0]) / NORM_H_COS[1]
    s_n = (s - NORM_S[0]) / NORM_S[1]
    l_n = (l - NORM_L[0]) / NORM_L[1]

    # Find k nearest neighbors by Euclidean distance
    distances = []
    for cluster_id, rep_h_sin, rep_h_cos, rep_s, rep_l in REPRESENTATIVES:
        # Normalize the representative point
        rh_sin_n = (rep_h_sin - NORM_H_SIN[0]) / NORM_H_SIN[1]
        rh_cos_n = (rep_h_cos - NORM_H_COS[0]) / NORM_H_COS[1]
        rs_n = (rep_s - NORM_S[0]) / NORM_S[1]
        rl_n = (rep_l - NORM_L[0]) / NORM_L[1]

        dist = math.sqrt(
            (h_sin_n - rh_sin_n) ** 2
            + (h_cos_n - rh_cos_n) ** 2
            + (s_n - rs_n) ** 2
            + (l_n - rl_n) ** 2
        )
        distances.append((dist, cluster_id))

    # Sort by distance, take k nearest
    distances.sort()
    neighbors = distances[:K_NEIGHBORS]

    # Distance-weighted vote
    votes = {}
    for dist, cluster_id in neighbors:
        weight = 1.0 / (dist + 1e-9)
        votes[cluster_id] = votes.get(cluster_id, 0.0) + weight

    return max(votes, key=votes.get)


